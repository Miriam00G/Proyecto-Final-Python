print("Programa11. \n Programa de regla de tres simple \n")
a = float(input("Escriba el valor de A1 "))
b = float(input("Escriba el valor de B2 "))
c = float(input("Escriba el valor de C3 "))

A1 = float(a)
B2 = float(b)
C3 = float(c)

x = (B2 * C3) / A1

print("El resultado de una regla de tres simple es: ", x)
      
